package com.dev;

import java.util.Scanner;

public class BankAppEx {
	// 메뉴 : 1. 계좌생성 (번호, 예금주, 예금액)
	// 2. 예금 (번호, 예금액) > 최고 예금 금액 10만원
	// 3. 출금 (번호, 출금액) > 잔액보다 큰 금액 출금 못하도록.
	// 4. 잔액조회 (번호)
	// 5. 종료.

	// static 선언해주면 인스턴트 실행 안해도된다.
	static BankApp[] banks = new BankApp[100];
	static Scanner scn = new Scanner(System.in);

	public static void main(String[] args) {
		while (true) {
			printMenu();
			int menu = scn.nextInt();
			if (menu == 1) {
				createAcc();
			} else if (menu == 2) {
				deposit();
			} else if (menu == 3) {
				withdraw();
			} else if (menu == 4) {
				checkAcc();
			} else if (menu == 5) {
				System.out.println("프로그램을 종료합니다.");
				break;
			} else if (menu == 9) {
				showList();
			}
			System.out.println("=== End ===");
		} // end of while.

	}

	// 메뉴출력 기능
	public static void printMenu() {
		String menu = "1. 계좌생성 (번호, 예금주, 예금액)\r\n" + "2. 예금 (번호, 예금액)\r\n" + "3. 출금 (번호, 출금액)\r\n" + "4. 잔액조회 (번호)\r\n"
				+ "5. 종료.\r\n" + "<선택>";
		System.out.println(menu);
	}

	// 계좌생성
	public static void createAcc() {
		System.out.println("계좌생성");
		System.out.println("계좌번호입력>>");
		String accNo;

		// 계좌번호 있는지 체크
		while (true) {
			accNo = scn.next();
			if (schAccNo(accNo) != null) {
				System.out.println("이미 존재하는 계좌번호 입니다.");
				System.out.println("다시 입력하세요");
			} else {
				break;
			}

		}

		System.out.println("예금주입력>>");
		String accNme = scn.next();
		System.out.println("예금액입력>>");
		int money = scn.nextInt();

		BankApp accnt = new BankApp(accNo, accNme, money);

		for (int i = 0; i < banks.length; i++) {
			if (banks[i] == null) {
				banks[i] = accnt;
				break;
			}
		}
	}

	// 예금
	public static void deposit() {
		System.out.println("예금기능");
		System.out.println("계좌번호>>");
		String ano = scn.next();
		System.out.println("예금액을 입력하세요");
		int amt = scn.nextInt();
		int checkCnt = 0; // 조회가 되었는지 체크하는 변수

		for (int i = 1; i < banks.length; i++) {
			if (banks[i] != null && banks[i].getAccNo().equals(ano)) {
				// 계좌가 조회되었을 때 실행
				checkCnt = 1;
				int currAmt = banks[i].getMoney();

				// 예금액이 10만원을 초과하지 못하도록
				if (currAmt + amt > 100000) {
					checkCnt = 2;
					break;
				} else {
					banks[i].setMoney(currAmt + amt); // 잔액 + 입금액
					break;
				}
			}
		}

		if (checkCnt == 1) {
			System.out.println("정상적으로 처리되었습니다.");
		} else if (checkCnt == 2) {
			System.out.println("한도액을 초과했습니다.");
		} else {
			System.out.println("당신의 계좌는 너굴맨이 처리했다구!");
		}
	}

	// 출금
	public static void withdraw() {
		System.out.println("출금기능");
	}

	// 잔액조회
	public static void checkAcc() {
		System.out.println("잔액조회");
	}

	// 전체리스트 출력
	public static void showList() {
		for (int i = 0; i < banks.length; i++) {
			if (banks[i] != null) {
				System.out.println(banks[i].toString());
			}
		}
	}

	// 계좌번호를 입력하면 배열(banks)에서 그 계좌번호를 반환 없으면 null
	// 중복체크
	public static BankApp schAccNo(String accNo) {
		for (int i = 0; i < banks.length; i++) {
			if (banks[i] != null && banks[i].getAccNo().equals(accNo)) {
				return banks[i];
				// BankApp 클래스를 리턴한다.
			}

		}
		return null; // 반환 되는 값이 없다 -> null (모든 클래스에 대응되는 값, 모든 클래스의 초기값도 될 수 있다)
	}
}
